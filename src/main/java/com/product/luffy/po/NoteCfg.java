package com.product.luffy.po;

public class NoteCfg {

    private String noteId;
    private String noteCfgCd;
    private String noteCfgStatCd;
    private String noteCfgVal;
    private String regUserId;

    public String getNoteId() {
        return noteId;
    }

    public void setNoteId(String noteId) {
        this.noteId = noteId;
    }

    public String getNoteCfgCd() {
        return noteCfgCd;
    }

    public void setNoteCfgCd(String noteCfgCd) {
        this.noteCfgCd = noteCfgCd;
    }

    public String getNoteCfgStatCd() {
        return noteCfgStatCd;
    }

    public void setNoteCfgStatCd(String noteCfgStatCd) {
        this.noteCfgStatCd = noteCfgStatCd;
    }

    public String getNoteCfgVal() {
        return noteCfgVal;
    }

    public void setNoteCfgVal(String noteCfgVal) {
        this.noteCfgVal = noteCfgVal;
    }

    public String getRegUserId() {
        return regUserId;
    }

    public void setRegUserId(String regUserId) {
        this.regUserId = regUserId;
    }
}
